saldo = 200
saque = 200

print(f'saldo é maior que saque? {saldo > saque}')
print(f'saldo é maior ou igual a saque? {saldo >= saque}')
print(f'saldo é menor que saque? {saldo < saque}')
print(f'saldo é menor ou igual a saque? {saldo <= saque}')
print(f'saldo é igual a saque? {saldo == saque}')
print(f'saldo é diferente de saque? {saldo != saque}')
print(f'saldo é igual a saque? {saldo is saque}')
print(f'saldo é diferente de saque? {saldo is not saque}')
